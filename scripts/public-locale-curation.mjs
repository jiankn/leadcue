export function applyReplacementRules(locale, value) {
  return (localeReplacementRules[locale] ?? []).reduce((current, [pattern, replacement]) => current.replace(pattern, replacement), value);
}

export function getExpectedOverride(locale, sourceValue) {
  return localeStringOverrides[locale]?.[sourceValue];
}

export function curateLocalizedString(locale, sourceValue, translatedValue) {
  const exactOverride = getExpectedOverride(locale, sourceValue);

  if (exactOverride) {
    return exactOverride;
  }

  let result = translatedValue;

  return applyReplacementRules(locale, result);
}
